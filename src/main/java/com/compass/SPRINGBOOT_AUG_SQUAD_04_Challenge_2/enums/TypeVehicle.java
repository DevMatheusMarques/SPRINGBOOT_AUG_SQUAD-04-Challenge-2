package com.compass.SPRINGBOOT_AUG_SQUAD_04_Challenge_2.enums;

/**
 * Enumeration representing the different types of vehicles.
 * This is used to classify vehicles based on their physical type and function.
 */
public enum TypeVehicle {

    /**
     * Represents passenger cars, typically standard vehicles for personal use.
     */
    PASSENGER_CAR,

    /**
     * Represents motorcycles.
     */
    MOTORCYCLE,

    /**
     * Represents delivery trucks, primarily used for deliveries.
     */
    DELIVERY_TRUCK,

    /**
     * Represents public service vehicles, such as emergency or government service vehicles.
     */
    PUBLIC_SERVICE
}

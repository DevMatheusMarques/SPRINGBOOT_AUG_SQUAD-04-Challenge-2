package com.compass.SPRINGBOOT_AUG_SQUAD_04_Challenge_2.enums;

/**
 * Enumeration representing the different categories of vehicles.
 * This is used to categorize vehicles based on their type of use or registration status.
 */
public enum Category {

    /**
     * Represents vehicles that are monthly payers.
     */
    MONTHLY_PAYER,

    /**
     * Represents single-use vehicles, typically avulsos.
     */
    SINGLE,

    /**
     * Represents delivery trucks.
     */
    DELIVERY_TRUCK,

    /**
     * Represents public service vehicles.
     */
    PUBLIC_SERVICE
}
